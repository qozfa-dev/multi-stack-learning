// Variables in JavaScript

// 'let' can be reassigned
let city = 'London';
city = 'Paris';

// 'const' cannot be reassigned
const age = 30;

// 'var' can be used (less common now)
var country = 'Scotland';
var country = 'Spain'; // can be redeclared


console.log(city); // Output: Paris
console.log(age); // Output: 30
console.log(country); // Output: Spain
